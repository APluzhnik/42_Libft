#include "../inc/libft.h"

void	ft_strclr(char *s)
{
	char			*str;
	unsigned int	i;

	str = (char *)s;
	i = 0;
	if (!str)
		return ;
	while (str[i])
	{
		str[i] = '\0';
		i++;
	}
}
